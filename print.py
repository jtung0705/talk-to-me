import sys
import time
from time import sleep

def print_lyrics():
    lines = [
        ("\nWish you'd just talk to me", 0.05),
        ("talk to me :3", 0.05),
        ("talk to~~~\n", 0.06),        
    ]
    delays = [0.6, 00.3, 0.0]
    for i, (line, char_delay) in enumerate(lines):
        for char in line:
            print(char, end='')
            sys.stdout.flush()
            sleep(char_delay)
        time.sleep(delays[i])
        print('')

print_lyrics()

